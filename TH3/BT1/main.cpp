#include <bits/stdc++.h>
#include "phanso.h"
using namespace std;

int main()
{
    PhanSo a, b;
    cin>>a;
    cin>>b;
    cout<<"a + b = "<<a+b<<endl;
    cout<<"a - b = "<<a-b<<endl;
    cout<<"a * b = "<<a*b<<endl;
    cout<<"a / b = "<<a/b<<endl;
    cout<<"a == b: "<<(a==b)<<endl;
    cout<<"a != b: "<<(a!=b)<<endl;
    cout<<"a >= b: "<<(a>=b)<<endl;
    cout<<"a <= b: "<<(a<=b)<<endl;
    cout<<"a > b: "<<(a>b)<<endl;
    cout<<"a < b: "<<(a<b)<<endl;
    return 0;
}
